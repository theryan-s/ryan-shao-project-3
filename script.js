//'quiz' section, based off of 4 different questions with 2 buttons each
//event listeners for each category
//add event listener for submit
//once submission is made, obtain users choice to find results
//results display on the right side

function randomItem(optionsArray) {
    const index = Math.floor(Math.random() * optionsArray.length);
    return optionsArray[index];
}

$(document).ready(function(){
    //Force scroll position to top at page refresh
    $('html, body').animate({scrollTop: 0}, 100);

    //Slow scroll from start to first slide
    $('.start').on('click', function() {
        $('html, body').animate({
            scrollTop: $(".questions").offset().top
        }, 
        'slow'
        );
    });

    for(let i = 1; i <= 4; i++){
        if(i <=2 ){
            $(`.input${i}`).click(function() {
                $('html,body').animate({
                    scrollTop: $(`.question${i+1}`).offset().top
                },
                    'slow');
            });
        } else{
            $(`.input${i}`).click(function() {
                $('html,body').animate({
                    scrollTop: $(`.submit`).offset().top
                },
                    'slow');
            });
        }
    }
    
    $('form').on('submit', function(event){
        event.preventDefault();

        //Get User's choice and store in variable
        const userSize = $('input[name=size]:checked').val();
        const userMaintenance = $('input[name=maintenance]:checked').val();
        const userTrainability = $('input[name=trainability]:checked').val();
        const userActiveness = $('input[name=activeness]:checked').val();

        //Filter based off of user's size selection
        const userChoice = breeds[userSize];

        console.log(userSize);
        console.log(userMaintenance);
        console.log(userTrainability);
        console.log(userActiveness);

        //Exclude empty options
        if (!userSize || !userMaintenance || !userTrainability || !userActiveness){
            alert("Please fill out all the questions!");
            return;
        };

        //Find match between user selected options with list

        const finalOptions = userChoice.filter((breed) => {
            return (Number(userMaintenance) === breed.maintenance) && 
            (Number(userTrainability) === breed.trainability) &&
            (Number(userActiveness) === breed.activeness);
        }); 

        //Given the results of objects, randomize a dog breed for user
        const result = randomItem(finalOptions);

        //Create variables for the image and alt text path
        const imgURL = result.url;
        const altText = result.alt;
        
        //Display results
        $('.choice').text(`You should get a ${result.dogbreed}!`);
        $('.result').css("background-image", "url(" + imgURL + ")");
        $('.result').css('display', 'flex');

        //Smooth scroll down to results
        $('html, body').animate({
            scrollTop: $(".result").offset().top
        }, 
        'slow');
        });
    
    //Try again button
    $('.restart').on('click', function(){
        location.reload(true);
        $('html').scrollTop(0);
    });
});

